class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
        // Code here
        Node *ne=new Node(data);
        Node *h=head;
        Node *p=head;
        
        if(head->data>data)
        {
            ne->next=head;
            return ne;
        }
        else
        {
            while(h)
            {
                if(h->data>data)break;
                p=h;
                h=h->next;
            }
            if(h==NULL)
            {
                p->next=ne;
                return head;
            }
            else
            {
                p->next=ne;
                ne->next=h;
                return head;
            }
            return head;
        }
    }
};


